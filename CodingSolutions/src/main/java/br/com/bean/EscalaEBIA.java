package br.com.bean;

public class EscalaEBIA {

	private String idEscalaEbia;
	private String ebiaUm;
	private String ebiaDois;
	private String ebiaTres;
	private String ebiaQuatro;
	private String ebiaCinco;
	private String ebiaSeis;
	private String ebiaSete;
	
	public EscalaEBIA() {
		
	}

	public EscalaEBIA(String idEscalaEbia, String ebiaUm, String ebiaDois, String ebiaTres, String ebiaQuatro, String ebiaCinco,
			String ebiaSeis, String ebiaSete) {
		this.idEscalaEbia = idEscalaEbia;
		this.ebiaUm = ebiaUm;
		this.ebiaDois = ebiaDois;
		this.ebiaTres = ebiaTres;
		this.ebiaQuatro = ebiaQuatro;
		this.ebiaCinco = ebiaCinco;
		this.ebiaSeis = ebiaSeis;
		this.ebiaSete = ebiaSete;
	}

	public String getIdEscalaEbia() {
		return idEscalaEbia;
	}

	public void setIdEscalaEbia(String idEscalaEbia) {
		this.idEscalaEbia = idEscalaEbia;
	}

	public String getEbiaUm() {
		return ebiaUm;
	}

	public void setEbiaUm(String ebiaUm) {
		this.ebiaUm = ebiaUm;
	}

	public String getEbiaDois() {
		return ebiaDois;
	}

	public void setEbiaDois(String ebiaDois) {
		this.ebiaDois = ebiaDois;
	}

	public String getEbiaTres() {
		return ebiaTres;
	}

	public void setEbiaTres(String ebiaTres) {
		this.ebiaTres = ebiaTres;
	}

	public String getEbiaQuatro() {
		return ebiaQuatro;
	}

	public void setEbiaQuatro(String ebiaQuatro) {
		this.ebiaQuatro = ebiaQuatro;
	}

	public String getEbiaCinco() {
		return ebiaCinco;
	}

	public void setEbiaCinco(String ebiaCinco) {
		this.ebiaCinco = ebiaCinco;
	}

	public String getEbiaSeis() {
		return ebiaSeis;
	}

	public void setEbiaSeis(String ebiaSeis) {
		this.ebiaSeis = ebiaSeis;
	}

	public String getEbiaSete() {
		return ebiaSete;
	}

	public void setEbiaSete(String ebiaSete) {
		this.ebiaSete = ebiaSete;
	}
	
}

